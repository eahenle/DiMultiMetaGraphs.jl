# ╔═╡ 3d156fa6-fc27-4579-a843-1c9fa9711358
begin
	import Graphs.add_edge!
	function add_edge!(g::DMMG, v::Int, w::Int, d::NamedTuple=(;))
		ep = has_edge(g, v, w) ? push!(props(g, v, w), d) : [d]
		add_edge!(g.graph, v, w)
		g.eprops[SimpleEdge(v, w)] = ep
	end
end;

