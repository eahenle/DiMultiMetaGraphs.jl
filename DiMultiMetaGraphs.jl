mutable struct DiMultiMetaGraph <: AbstractMetaGraph{Float64}
	graph::DiMultigraph
	weightfield::Symbol
	defaultweight::Float64
	gprops::Dict
	vprops::Dict
	eprops::Dict
	
	DiMultiMetaGraph(nv::Int=0) = new(
		DiMultigraph(SimpleDiGraph{Int}(nv)),
		:weight,
		1.,
		Dict(),
		Dict(),
		Dict()
	)
end

